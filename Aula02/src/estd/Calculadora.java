package estd;

public class Calculadora {

	/*Subtrair
	 * Multiplicar
	 * Dividir*/
	
	public static int somar(int a, int b){
		return a + b;
	}
	
	public static int subtrair(int a, int b){
		return a- b;
	}
	
	public static int multiplicar(int a, int b){
		return a * b;
	}
	
	public static int dividir(int a, int b){
		return a / b;
	}
	
	
}
